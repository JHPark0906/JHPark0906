while True:
    str = input()
    if str == "CU":
        print("see you")
    elif str == ":-)":
        print("I’m happy")
    elif str == ":-(":
        print("I’m unhappy")
    elif str == ";-)":
        print("wink")
    elif str == ":-P":
        print("stick out my tongue")
    elif str == "(~.~)":
        print("sleepy")
    elif str == "TA":
        print("totally awesome")
    elif str == "CCC":
        print("Canadian Computing Competition")
    elif str == "CUZ":
        print("because")
    elif str == "TY":
        print("thank-you")
    elif str == "YW":
        print("you’re welcome")
    elif str == "TTYL":
        print("talk to you later")
        break
    else:
        print(str)